// Оголошення класу Node з параметром типу T (узагальнення)
public class Node<T> {

    // Змінна для зберігання значення вузла будь-якого типу T
    public T value;

    // Змінна для зберігання посилання на наступний вузол у списку
    public Node<T> next;

    // Конструктор класу, який приймає значення і ініціалізує поле value
    public Node(T value) {
        this.value = value; // Присвоєння значення полю value
    }
}